/* 
 * bang - Compute !x without using !
 *   Examples: bang(3) = 0, bang(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4 
 */
int bang(int x) {
  int minus = ~x + 1;
  int sig1 = (x >> 31) & 1;
  int sig2 = (minus >> 31) & 1;
  return ~(sig1 | sig2) & 1;
  //comp
}
/*
 * bitCount - returns count of number of 1's in word
 *   Examples: bitCount(5) = 2, bitCount(7) = 3
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 40
 *   Rating: 4
 */
int bitCount(int x) {
  int mask10 = (0x55 << 8) + 0x55;     //0x5555 ops.2
  int mask1 = (mask10 << 16) + mask10; //0x55555555 ops.2
  int mask20 = (0x33 << 8) + 0x33;     //0x3333 ops.2
  int mask2 = (mask20 << 16) + mask20; //0x33333333 ops.2
  int mask30 = (0x0f << 8) + 0x0f;     //0x0f0f ops.2
  int mask3 = (mask30 << 16) + mask30; //0x0f0f0f0f ops.2
  int mask4 = (0xff << 16) + 0xff;     //0x00ff00ff ops.2
  int mask5 = (0xff << 8) + 0xff;      //0x0000ffff ops.2
  int val1 = (x & mask1) + ((x >> 1) & mask1); //ops.4
  int val2 = (val1 & mask2) + ((val1 >> 2) & mask2);//ops.4
  int val3 = (val2 + (val2 >> 4)) & mask3;//ops.3
  int val4 = (val3 + (val3 >> 8)) & mask4;//ops.3
  int val5 = (val4 + (val4 >> 16)) & mask5;//ops.4
  return val5;
  //comp
}
